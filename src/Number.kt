fun main() {
    // Integer types
    val age : Byte = 27
    val weight : Short = 120
    val distance : Int = 86000
    val balance : Long = 3000000000L

    println(age)
    println(weight)
    println(distance)
    println(balance)

    // Unsigned integer types
    val uAge : UByte = 27u
    val uWeight : UShort = 120u
    val uDistance : UInt = 86000U
    val uBalance : ULong = 3000000000uL

    println(uAge)
    println(uWeight)
    println(uDistance)
    println(uBalance)
}